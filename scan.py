from __future__ import annotations

import argparse
import json
import os
import shutil
import sys
from dataclasses import asdict, dataclass
from datetime import datetime, timezone
from pathlib import Path
from typing import Iterator


@dataclass(frozen=True)
class ScanCategory:
    id: str
    name: str
    description: str
    path: str


@dataclass
class ChildItem:
    path: str
    size_bytes: int
    item_count: int
    is_whitelisted: bool
    matched_filter: bool


@dataclass
class CategoryResult:
    id: str
    name: str
    description: str
    root_path: str
    exists: bool
    file_count: int
    total_size_bytes: int
    child_items: list[ChildItem]
    warnings: list[str]


@dataclass
class ScanReport:
    scanned_at: str
    total_size_bytes: int
    total_files: int
    category_count: int
    app_filter: str | None
    whitelist: list[str]
    categories: list[CategoryResult]


@dataclass
class DeletionResult:
    deleted_paths: list[str]
    skipped_paths: list[str]
    errors: list[str]


